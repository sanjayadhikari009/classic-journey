                if (scrollTop > lastScrollTop) {
                    // Scrolling down
                    applyScrollDownStyles();
                } else if (scrollTop < lastScrollTop || scrollTop === 0) {
                    // Scrolling up or at top
                    if (scrollTop === 0) {
                        applyScrollUpStyles();
                    }
                }
            }

